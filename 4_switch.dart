void main(){
  String grade = 'D';
  // Switch case statements are applicable for string and integer values only
  switch(grade){
    case 'A':
      print("super grade. excellent");
      break;
    case 'B':
      print("good grade");
      break;
    case 'C':
      print("you can do better.");
      break;
    default:
      print("you need to work hard");
  }
  print("finished");
}
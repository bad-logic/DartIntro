void main(){

  //  FOR LOOP
  for(var i=0;i<5;i++){
    print(i);
  }

  //  WHILE LOOP
  // WILL NOT BE EXECUTED IF CONDITION IS NOT SATISFIED
  int j = 0;
  while ( j < 5 ){
    print(j);
    j++;
  }

  // DO WHILE LOOP
  // WILL BE EXECUTED AT LEAST ONCE
  int k = 6;
  do {
    print(k);
    k++;
  } while ( k < 5 );

}